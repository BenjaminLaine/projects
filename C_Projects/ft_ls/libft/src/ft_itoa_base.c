#include "ft_printf.h"

char	*ft_itoa_base(long long n, int b, int up)
{
	char				*str;
	unsigned long long	z[3];

	z[2] = 1;
	z[1] = n < 0 ? 1 : 0;
	z[0] = n < 0 ? -n : n;
	(b != 10 && z[1]) ? z[1] = 0 : 1;
	while (((n > 0) || (n < 0)) && (n = n < 0 ? ((n * -1) / b) : (n / b)))
		z[2]++;
	if (!(str = ft_strnew(z[2] + z[1])) || b < 2 || b > 16)
		return (NULL);
	while ((z[2]-- + z[1] > 0))
	{
		if (b > 10 && (z[0] % b >= 10) && up)
			str[z[2] + z[1]] = ((z[0] % b) - 10 + 'A');
		else if (b > 10 && (z[0] % b >= 10))
			str[z[2] + z[1]] = ((z[0] % b) - 10 + 'a');
		else
			str[z[2] + z[1]] = ((z[0] % b) + 48);
		z[0] /= b;
	}
	z[1] == 1 ? str[0] = '-' : 1;
	return (str);
}
